/** Clipart shape: 16×16 boolean grid. `true` = filled cell, `false` = empty */
export interface ClipartShape {
  id: string;
  name: string;
  category: string;
  grid: boolean[][];
  width: number;
  height: number;
}

/** All shape categories */
export const SHAPE_CATEGORIES = [
  'Animals', 'Nature', 'Flowers', 'Holiday', 'Food', 'Symbols', 'Borders', 'Geometric'
] as const;

export type ShapeCategory = typeof SHAPE_CATEGORIES[number];

// ── Helper to create a shape from compact string notation ──
// '.' = empty, '#' = filled
function s(rows: string[]): boolean[][] {
  return rows.map(row => [...row].map(ch => ch === '#'));
}

