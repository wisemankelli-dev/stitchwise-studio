/**
 * Bitmap font glyphs for the Alphabet tool in the embroidery Designer.
 * Each glyph is a 5x7 boolean grid (true = stitch, false = empty).
 * Supports A-Z uppercase letters.
 */

export type FontGlyph = number[][]; // 5 columns x 7 rows (0 = empty, 1 = stitch)

export interface FontDefinition {
  id: string;
  name: string;
  glyphs: Record<string, FontGlyph>;
  charWidth: number;
  charHeight: number;
  spacing: number; // pixels between characters
}

