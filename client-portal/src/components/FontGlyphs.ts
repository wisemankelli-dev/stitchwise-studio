/** Renders a text string onto the grid using the given font definition */
export function renderTextToGrid(
  text: string,
  font: FontDefinition,
  startRow: number,
  startCol: number,
  color: string,
  stitchType: string,
  gridSize: number,
  setCell: (row: number, col: number, color: string, stitch: string) => void,
): void {
  const { glyphs, charWidth, charHeight, spacing } = font;
  const upperText = text.toUpperCase();
  let cursorCol = startCol;

  for (const char of upperText) {
    if (char === ' ') {
      cursorCol += charWidth + spacing;
      continue;
    }
    const glyph = glyphs[char];
    if (!glyph) {
      cursorCol += charWidth + spacing;
      continue;
    }

    for (let r = 0; r < charHeight; r++) {
      for (let c = 0; c < charWidth; c++) {
        if (glyph[r]?.[c]) {
          const gridRow = startRow + r;
          const gridCol = cursorCol + c;
          if (gridRow >= 0 && gridRow < gridSize && gridCol >= 0 && gridCol < gridSize) {
            setCell(gridRow, gridCol, color, stitchType);
          }
        }
      }
    }
    cursorCol += charWidth + spacing;
  }
}
